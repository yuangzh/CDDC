% function demo_accuracy4_L4EIG
% min_x F(x) = x'G'Gx + beta ||x||_4^4, s.t. ||x|| = 1
clc;clear all;close all;
rand('seed',1); randn('seed',1);
addpath('util','solver','proximal','data');


id_data = [11 12 13 21 22 23 31 32 33 41 42 43 51 52 53 61 62 63 ];

times = 10;
result = [];
for idat = 1:length(id_data)
    
    avg_his1 = [];
    avg_his2 = [];
    avg_his3 = [];
    avg_his4 = [];
    
    [G,DataStr] = GetDataSet(id_data(idat),1);
    A = G'*G; n = size(G,2);beta = 1000;
    F = @(x)L4EIG_ComputeTrueObj(x,A,beta);
    for it = 1:times
        rand('seed',it); randn('seed',it);
        x0 = randn(n,1);
        stopCriteria = 1e-5; % Stop the algorithm when the relative change in F(x) falls below stopCriteria
        timeLimit = 1000;    % Stop the algorithm when the time exceeds timeLimit seconds
        timeIntervel = 1;    % Record the objective value in every timeIntervel second
        [x1,fobjs1,ts1] = L4EIG_MSCR(x0,A,beta,stopCriteria,timeLimit,timeIntervel);
        [x2,fobjs2,ts2] = L4EIG_PDCA(x0,A,beta,stopCriteria,timeLimit,timeIntervel);
        [x3,fobjs3,ts3] = L4EIG_GProj(x0,A,beta,stopCriteria,timeLimit,timeIntervel);
        [x4,fobjs4,ts4] = L4EIG_SCA(x0,A,beta,stopCriteria,60,timeIntervel); % this algorithm could be very slow! We stop it after 60 seconds
        [x5,fobjs5,ts5] = L4EIG_SNCA(x0,A,beta,stopCriteria,timeLimit,timeIntervel);
        fprintf('fobj: %f %f %f %f %f, time spent:%f %f %f %f %f \n',F(x1),F(x2),F(x3),F(x4),F(x5),max(ts1),max(ts2),max(ts3),max(ts4),max(ts5));
        avg_his1(it) = F(x1); avg_his2(it) = F(x2); avg_his3(it) = F(x3); avg_his4(it) = F(x4);avg_his5(it) = F(x5);
    end
    
    One = [];
    One.his1 = [mean(avg_his1);std(avg_his1)];
    One.his2 = [mean(avg_his2);std(avg_his2)];
    One.his3 = [mean(avg_his3);std(avg_his3)];
    One.his4 = [mean(avg_his4);std(avg_his4)];
    One.his5 = [mean(avg_his5);std(avg_his5)];
    result{idat} = One; save(mfilename,'result')
    
    fprintf('%s & ',DataStr);
    fprintf('%.3f $\\pm$ %.3f & ',mean(avg_his1),std(avg_his1));
    fprintf('%.3f $\\pm$ %.3f & ',mean(avg_his2),std(avg_his2));
    fprintf('%.3f $\\pm$ %.3f & ',mean(avg_his3),std(avg_his3));
    fprintf('%.3f $\\pm$ %.3f & ',mean(avg_his4),std(avg_his4));
    fprintf('%.3f $\\pm$ %.3f  ',mean(avg_his5),std(avg_his5));
    fprintf('\\\\');
    fprintf('\n');
end



